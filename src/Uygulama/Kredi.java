    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(jTextField9.getText().trim().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Kredi Mikatrını Giriniz");
        else if(jTextField10.getText().trim().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Taksit sayısı giriniz");
        else if(jTextField11.getText().trim().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Faiz Oranını giriniz");
        else{
        
           if(!(vld.rakamkontrol(jTextField9.getText().trim())))
               JOptionPane.showMessageDialog(rootPane, "Kredi miktarı sadece rakamlardan oluşmalı");
           else if(!(vld.rakamkontrol(jTextField10.getText().trim())))
               JOptionPane.showMessageDialog(rootPane, "Taksit sayısı rakamlardan oluşmalı");
       
           else
           {
               
               int aa=Integer.valueOf(jTextField9.getText().trim());
               int bb=(Integer.valueOf(jTextField5.getText().trim())+1)*10;
           
                 if(aa>bb)
             JOptionPane.showMessageDialog(rootPane, "Kredi Miktarı en fazla maaşın 10 katı olmalı");
                 if(Integer.valueOf(jTextField10.getText().trim())%6!=0)
                     JOptionPane.showMessageDialog(rootPane, "Taksit Sayısı 6 veya Katları olmalı");
                 else
                 {
                 
                double faizz=(Integer.valueOf(jTextField10.getText().trim()))/6*(Double.valueOf(jTextField11.getText().trim()));
                
                    int tutar=(int) (Integer.valueOf(jTextField9.getText().trim())+Integer.valueOf(jTextField9.getText().trim())*faizz/100);
                    jTextField12.setText(tutar+"");
                    jTextField12.setEditable(false);
                    
                   int taksittutar=tutar/(Integer.valueOf(jTextField10.getText().trim()));
                    jTextField13.setText(taksittutar+"");
                    jTextField13.setEditable(false);
              
                 
                 }
     
           }
    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

