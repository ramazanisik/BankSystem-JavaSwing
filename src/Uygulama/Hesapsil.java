    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

      if(jTextField1.getText().trim().equals(""))
          JOptionPane.showMessageDialog(rootPane, "TC Giriniz");
      else
      {
      
      String q="call hesapsil_ara(?)";
          PreparedStatement pr=db.preBaglan(q);
          try {
              pr.setString(1, jTextField1.getText().trim());
              ResultSet rs=pr.executeQuery();
              DefaultTableModel dtm=new DefaultTableModel();
               dtm.addColumn("Hesap ID");
               dtm.addColumn("Hesap Türü");
               dtm.addColumn("Borç Durumu");
               
              while(rs.next())
              {
              
                  dtm.addRow(new String[] {rs.getString(1),rs.getString(2),rs.getString(4)});
              }
              jTable1.setModel(dtm);
              jPanel3.setVisible(true);
              
          } catch (SQLException ex) {
              Logger.getLogger(Hesapsil.class.getName()).log(Level.SEVERE, null, ex);
          }
          finally{db.kapat();}
      
      
      
      
      }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int x=jTable1.getSelectedRow();
        
        if(x!=-1)
        {
            if(Integer.valueOf(jTable1.getValueAt(x, 2).toString())>0)
                JOptionPane.showMessageDialog(rootPane, "Lütfen Önce Borcu Ödeyiniz");
            else
            {
               String q="call hesapsil(?) ";
               PreparedStatement pr=db.preBaglan(q);
                try {
                    pr.setString(1, jTable1.getValueAt(x, 0).toString());
                    pr.execute();
                    
                    JOptionPane.showMessageDialog(rootPane, "Hesap Silme Başarılı");
                    dispose();
                    
                } catch (SQLException ex) {
                    System.out.println("Hesap Silme Hatası :"+ex);
                }
               
            
            
            
            }
     
        
        
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed


