    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

       if(jTextField2.getText().trim().equals(""))
           JOptionPane.showMessageDialog(rootPane, "Lütfen Limit Giriniz");
       else if(jTextField3.getText().trim().equals(""))
           JOptionPane.showMessageDialog(rootPane, "Lütfen Ekstre Tarihi Giriniz");
       else
       {
       String q="call hesap_guncelle(?,?,?)";
       
       PreparedStatement pr=db.preBaglan(q);
           try {
               pr.setString(1, jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
               pr.setString(2, jTextField2.getText().trim());
               pr.setString(3, jTextField3.getText().trim());
               pr.execute();
               
               JOptionPane.showMessageDialog(rootPane, "Hesap Güncelleme Başarılı");
               jTextField2.setText("");
               jTextField3.setText("");
               jPanel4.setVisible(false);
           } catch (SQLException ex) {
               Logger.getLogger(Hesapguncelle.class.getName()).log(Level.SEVERE, null, ex);
           }
       finally{db.kapat();}
       
       
       
       
       
       
       
       }
    }//GEN-LAST:event_jButton3ActionPerformed


