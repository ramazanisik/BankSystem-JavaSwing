    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    jPanel4.setVisible(false);
    jPanel3.setVisible(false);
      if(jTextField1.getText().trim().equals(""))
          JOptionPane.showMessageDialog(rootPane, "TC Giriniz");
      else
      {
      
      String q="call hesapsil_ara(?)";
          PreparedStatement pr=db.preBaglan(q);
          try {
              pr.setString(1, jTextField1.getText().trim());
              ResultSet rs=pr.executeQuery();
              DefaultTableModel dtm=new DefaultTableModel();
               dtm.addColumn("Hesap ID");
               dtm.addColumn("Hesap Türü");
               dtm.addColumn("Limit");
               dtm.addColumn("Ekstre Tarih");
               
              while(rs.next())
              {
              
                  dtm.addRow(new String[] {rs.getString(1),rs.getString(2),rs.getString(6),rs.getString(7)});
              }
              jTable1.setModel(dtm);
              jPanel3.setVisible(true);
              
          } catch (SQLException ex) {
              Logger.getLogger(Hesapguncelle.class.getName()).log(Level.SEVERE, null, ex);
          }
          finally{db.kapat();}
      
      
      
      
      }
    }//GEN-LAST:event_jButton1ActionPerformed

